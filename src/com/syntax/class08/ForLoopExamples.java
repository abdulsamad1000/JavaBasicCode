package com.syntax.class08;

public class ForLoopExamples {

	public static void main(String[] args) {
		// i need to print number from 1 to 90
		
		for (int i=1; i<=90; i++) {
			System.out.print(i+" ");
		}
		/*
		 * you have specify the start point 
		 * ending point
		 * increment / decrement
		 */
		System.out.println("-------------------------------------------");
		
		// i need to print number from 60 to 10
		for (int x=60; x>10; x--) {
			System.out.print(x+" ");
		}
		// what is the output
		for (int b=5; b<=40; b+=5){
			System.out.println(b+" ");
		}
		
	}

}
