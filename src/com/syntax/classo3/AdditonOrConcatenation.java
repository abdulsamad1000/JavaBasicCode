package com.syntax.classo3;

public class AdditonOrConcatenation {

	public static void main(String[] args) {
		
		
		int num=50;
		num+=500; // num=num+500 result would be 550
		num+=200;
		System.out.println(num); // 750
		int num2=150;
		num2-=60;
		System.out.println(num2); // 90
		double cakePiece=11;
		cakePiece/=4;
		System.out.println(cakePiece);
		
		double cake=25;
		cake%=7;
		System.out.println(cake);
		
		
		
		
		
		
		
		
	}

}
