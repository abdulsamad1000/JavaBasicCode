package com.syntax.class07;

public class WhileLoopsMoreExp {

	public static void main(String[] args) {
		int time=10;
		if (time<12) {
			System.out.println("Morining"); // 1
		}
		System.out.println("================================================");
		
		while (time<12) {
			System.out.println("Morning"); // infinite mean endless number 
			time++;
			
		}

	}

}
